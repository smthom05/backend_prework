# Numbers and Math

# + plus
# - minue
# / slash
# * asterisk
# % percent
# < less than
# > greater than
# <= less than or equal
# >= greater than or equal

# Tells us what we are counting
puts "I will now count my chickens:"

# Does the math for his chickens and roosters
puts "Hens #{25 + 30/6}"
puts "Roosters #{100 - 25 * 3 % 4}"

# Tells us what we are counting now
puts "Now I will count the eggs:"

# The math for the egg calculation
puts 3.2 + 2.2 + 1.1 - 5.33 + 4.44 % 2.0 - 1.3 / 4.0 + 6.2

# Asks us a truthy question
puts "Is it true that 3.0 + 2.0 < 5.0 - 7.0?"

# Does the math for the question
puts 3.0 + 2.0 < 5.0 - 7.0

# Explains how we got to our answer
puts "What is 3 + 2? #{3 + 2}"
puts "What is 5 - 7? #{5 - 7}"

# We understand why
puts "Oh, thats why it's false"

puts "How about some more?"

# A series of greater than, less than, or equal to questions
puts "Is it greater? #{5 > -2}"
puts "Is it greater or equal? #{5 >= -2}"
puts "Is it less or equal? #{5 <= -2}"
